#Operadores Aritmeticos

a = 11
b = 3

#Suma +

suma = a + b

print(f"La suma es: {suma}" )

#Resta -
resta = a - b
print(f"La resta es: {resta}")

#Multipliacion *
multiplicacion = a * b
print(f"El resultado de la multiplicacion es: {multiplicacion}")

#Division /  Retorna un tipo float

division = a/b
print(f"El resultado de la division es: {division:.2f}")

#Division // entera

division_entera = a//b
print (f" El resultado de la division entera es: {division_entera}")

# Modulo (%) residuo de la division
modulo = a%b
print(f"El modulo de la division es: {modulo}")

#Exponente (potencia) **
exponenente = a ** b
print(f"El resultado del exponente es: {exponenente}")

